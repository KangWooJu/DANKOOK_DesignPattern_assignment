public class Box {

    private int width;
    private int height;
    private int length;

    public Box(int width,int hight,int length){
        this.width = width;
        this.height = hight;
        this.length = length;
    }

    public int getWidth(){
        return width;
    }

    public int getHeight(){
        return height;
    }

    public int getLength(){
        return length;
    }
}
